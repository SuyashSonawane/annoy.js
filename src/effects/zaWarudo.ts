function getRandom(n) {
    var result = new Array(n),
        len = CSS_COLOR_NAMES.length,
        taken = new Array(len);
    if (n > len)
        throw new RangeError("getRandom: more elements taken than available");
    while (n--) {
        var x = Math.floor(Math.random() * len);
        result[n] = CSS_COLOR_NAMES[x in taken ? taken[x] : x];
        taken[x] = --len in taken ? taken[len] : len;
    }
    return result;
}
class ZaWarudo implements Effect {
    constructor(private wait = 0.25, private numColors = 5) { }

    private spheres: Array<HTMLElement> = [];
    private audio: HTMLAudioElement;
    actionDetected(data: FunctionDataPoint): void {
        const color = getRandom(this.numColors);
        document.body.style.overflow = "hidden"

        this.audio = document.createElement('audio')
        this.audio.src = "https://media1.vocaroo.com/mp3/1gfHBu9eh62R"

        this.audio.onloadstart = () => {
            this.audio.play()

            for (let i = 0; i < color.length; i++) {
                let sphere = document.createElement('div')
                sphere.className = 'za-warudo-sphere'
                sphere.style.top = data.elY + 'px'
                sphere.style.left = data.elX + 'px'
                sphere.style.backgroundColor = color[Math.floor(Math.random() * color.length)]
                // sphere.style.backgroundColor = color[i]
                sphere.style.animation = `zw-grow 5.2s ease forwards ${this.wait * i}s`
                this.spheres.push(sphere)
                document.body.appendChild(sphere)
            }
        }

    }
    actionTime(data: FunctionDataPoint): void {
    }
    dispose(data: FunctionDataPoint): void {
        while (this.spheres.length)
            document.body.removeChild(this.spheres.pop())
        this.audio.pause()
    }

}

export default ZaWarudo